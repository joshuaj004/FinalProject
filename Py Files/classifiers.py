def horizontal_position(matrix):
    ''' The horizontal position, counting pixels from the left edge of the image, of the center
    of the smallest rectangular box that can be drawn with all "on" pixels inside the box. '''
    division = int(sqrt(len(matrix)))
    for x in range(division):
        for y in range(division):
            if matrix[x + y * division] == 1:
                return x
    return division

def vertical_position(matrix):
    '''The vertical position, counting pixels from the bottom, of the above box.'''
    division = int(sqrt(len(matrix)))
    for x in range(len(matrix) - 1, -1, -1):
        if matrix[x] == 1:
            break
    return (len(matrix) - x) // division


def on_pixels(matrix):
    '''The total number of "on" pixels in the character image.'''
    return matrix.count(1)


def mean_horizontal_position(matrix):
    '''The mean horizontal position of all "on" pixels relative to the center of the box and divided by the width of
     the box. This feature has a negative value if the image is "leftheavy" as would be the case for the letter L.'''
    division = int(sqrt(len(matrix)))
    masterList = []
    for x in range(division):
        masterList.append(matrix[x::division])
    reward = -division//2
    mean_value = 0
    for x in masterList:
        mean_value += x.count(1) * reward
        reward += 1
    return mean_value//division


def mean_vertical_position(matrix):
    '''The mean vertical position of all "on" pixels relative to the center of the box and divided by the height of
     the box.'''
    division = int(sqrt(len(matrix)))
    masterList = []
    for x in range(division):
        masterList.append(matrix[x * division: (x + 1) * division])
    reward = -division//2
    mean_value = 0
    for x in masterList:
        mean_value += x.count(1) * reward
        reward += 1
    return mean_value//division


def mean_squared_horizontal_position(matrix):
    '''The mean squared value of the horizontal pixel distances as measured in mean_horizontal_position above. This
    attribute will have a higher value for images whose pixels are more widely separated in the horizontal direction
     as would be the case for the letters W or M.'''
    division = int(sqrt(len(matrix)))
    masterList = []
    for x in range(division):
        masterList.append(matrix[x::division])
    reward = -division//2
    mean_value = 0
    for x in masterList:
        mean_value += x.count(1) * reward ** 2
        reward += 1
    return mean_value//division


def mean_squared_vertical_position(matrix):
    '''The mean squared value of the vertical pixel distances as measured in mean_vertical_position above.'''
    division = int(sqrt(len(matrix)))
    masterList = []
    for x in range(division):
        masterList.append(matrix[x * division: (x + 1) * division])
    reward = -division//2
    mean_value = 0
    for x in masterList:
        mean_value += x.count(1) * reward ** 2
        reward += 1
    return mean_value//division


